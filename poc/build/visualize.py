"""Renders the object dependency graph as a Mermaid flowchart, color-coded by
blast-radius status. Deterministic, no AI involved - purely a view over the
same metadata + manifest the diff engine already reads.

Templates are drawn as their own nodes (dashed edges into the objects that use
them), not folded into object status - a template change is a distinct kind of
"dependency" from a depends_on edge, and previously wasn't visible in this
diagram at all even though the diff engine already tracked it."""
from pathlib import Path

from build.diff_engine import (
    TEMPLATES_DIR,
    changed_or_new_objects,
    compute_blast_radius,
    hash_file,
    load_all_metadata,
    load_manifest,
    load_selection,
)

BASE = Path(__file__).resolve().parent.parent
OUT_PATH = BASE / "blast_radius.md"


def _node_id(object_id: str) -> str:
    return object_id.replace(".", "_")


def _template_users(objects: dict) -> dict:
    """template_name -> set of object_ids currently selected to use it (per
    their selections/*.json, if one exists yet)."""
    users: dict[str, set] = {}
    for obj_id, data in objects.items():
        if "transformation" not in data:
            continue
        selection = load_selection(obj_id)
        if selection is None:
            continue
        users.setdefault(selection["template"], set()).add(obj_id)
    return users


def _changed_templates(template_users: dict, manifest: dict) -> set:
    """A template counts as changed if its current file hash doesn't match
    the template_hash recorded for ANY object currently using it - covers
    both "this template drifted since the last build" and "this object was
    just pointed at a template it's never been built with"."""
    changed = set()
    for template_name, users in template_users.items():
        template_path = TEMPLATES_DIR / f"{template_name}.py.tmpl"
        current_hash = hash_file(template_path) if template_path.exists() else None
        for obj_id in users:
            entry = manifest.get(obj_id)
            recorded_hash = entry.get("template_hash") if isinstance(entry, dict) else None
            if recorded_hash != current_hash:
                changed.add(template_name)
                break
    return changed


def render() -> Path:
    objects = load_all_metadata()
    manifest = load_manifest()

    changed_or_new = set(changed_or_new_objects(objects, manifest))
    blast_radius = set(compute_blast_radius())
    downstream_only = blast_radius - changed_or_new
    unaffected = set(objects) - blast_radius

    template_users = _template_users(objects)
    templates_changed = _changed_templates(template_users, manifest)
    templates_unaffected = set(template_users) - templates_changed

    lines = ["flowchart LR"]
    for obj_id, data in objects.items():
        kind = "generation target" if "transformation" in data else "source"
        lines.append(f'    {_node_id(obj_id)}["{obj_id}\\n({kind})"]')

    for obj_id, data in objects.items():
        for dep in data.get("depends_on", []):
            lines.append(f"    {_node_id(dep)} --> {_node_id(obj_id)}")

    for template_name, users in template_users.items():
        node = f"template_{template_name}"
        lines.append(f'    {node}(["template: {template_name}"])')
        for obj_id in sorted(users):
            lines.append(f"    {node} -.-> {_node_id(obj_id)}")

    changed_ids = {_node_id(o) for o in changed_or_new} | {
        f"template_{t}" for t in templates_changed
    }
    unaffected_ids = {_node_id(o) for o in unaffected} | {
        f"template_{t}" for t in templates_unaffected
    }

    if changed_ids:
        lines.append(f"    class {','.join(sorted(changed_ids))} changed")
    if downstream_only:
        ids = ",".join(sorted(_node_id(o) for o in downstream_only))
        lines.append(f"    class {ids} affected")
    if unaffected_ids:
        lines.append(f"    class {','.join(sorted(unaffected_ids))} unaffected")

    lines.append("    classDef changed fill:#f8b4b4,stroke:#c0392b,stroke-width:2px,color:#000")
    lines.append("    classDef affected fill:#fde9a0,stroke:#d4a017,stroke-width:2px,color:#000")
    lines.append("    classDef unaffected fill:#d5f5e3,stroke:#27ae60,stroke-width:1px,color:#000")

    mermaid_block = "\n".join(lines)

    summary = [
        "# Blast radius",
        "",
        "Regenerated by `python run.py graph`. Reflects metadata + templates vs. "
        "`manifest/hash_manifest.json` at the time it was rendered.",
        "",
        "**Legend:**",
        "- \U0001F534 red = metadata changed, template changed, or object/template is new "
        "(direct hit)",
        "- \U0001F7E1 yellow = unchanged itself, pulled in because something it "
        "`depends_on` changed (propagated impact) - only applies to data objects, "
        "not templates",
        "- \U0001F7E2 green = untouched, hash matches the manifest, not regenerated",
        "- rounded nodes connected by dashed lines are templates, not data objects - "
        "an object using a changed template is *itself* red/yellow via the diff "
        "engine's own template-hash tracking, independent of this diagram",
        "",
        "```mermaid",
        mermaid_block,
        "```",
        "",
    ]
    if changed_or_new or downstream_only:
        summary.append(
            f"**Pending:** {len(changed_or_new)} changed/new, "
            f"{len(downstream_only)} pulled in downstream, "
            f"{len(unaffected)} untouched."
        )
    else:
        summary.append("**Clean:** metadata matches the last successful build.")
    if templates_changed:
        summary.append(f"**Templates changed:** {', '.join(sorted(templates_changed))}.")
    if changed_or_new or downstream_only or templates_changed:
        summary.append("Run `python run.py diff` then `python run.py generate` to clear this.")

    OUT_PATH.write_text("\n".join(summary) + "\n")
    return OUT_PATH


if __name__ == "__main__":
    print(render())
