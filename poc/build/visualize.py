"""Renders the object dependency graph as a Mermaid flowchart, color-coded by
blast-radius status. Deterministic, no AI involved - purely a view over the
same metadata + manifest the diff engine already reads."""
from pathlib import Path

from build.diff_engine import (
    build_reverse_graph,
    changed_or_new_objects,
    compute_blast_radius,
    load_all_metadata,
    load_manifest,
)

BASE = Path(__file__).resolve().parent.parent
OUT_PATH = BASE / "blast_radius.md"


def _node_id(object_id: str) -> str:
    return object_id.replace(".", "_")


def render() -> Path:
    objects = load_all_metadata()
    manifest = load_manifest()

    changed_or_new = set(changed_or_new_objects(objects, manifest))
    blast_radius = set(compute_blast_radius())
    downstream_only = blast_radius - changed_or_new
    unaffected = set(objects) - blast_radius

    lines = ["flowchart LR"]
    for obj_id, data in objects.items():
        kind = "generation target" if "transformation" in data else "source"
        lines.append(f'    {_node_id(obj_id)}["{obj_id}\\n({kind})"]')

    for obj_id, data in objects.items():
        for dep in data.get("depends_on", []):
            lines.append(f"    {_node_id(dep)} --> {_node_id(obj_id)}")

    if changed_or_new:
        ids = ",".join(sorted(_node_id(o) for o in changed_or_new))
        lines.append(f"    class {ids} changed")
    if downstream_only:
        ids = ",".join(sorted(_node_id(o) for o in downstream_only))
        lines.append(f"    class {ids} affected")
    if unaffected:
        ids = ",".join(sorted(_node_id(o) for o in unaffected))
        lines.append(f"    class {ids} unaffected")

    lines.append("    classDef changed fill:#f8b4b4,stroke:#c0392b,stroke-width:2px,color:#000")
    lines.append("    classDef affected fill:#fde9a0,stroke:#d4a017,stroke-width:2px,color:#000")
    lines.append("    classDef unaffected fill:#d5f5e3,stroke:#27ae60,stroke-width:1px,color:#000")

    mermaid_block = "\n".join(lines)

    summary = [
        "# Blast radius",
        "",
        "Regenerated by `python run.py graph`. Reflects metadata vs. "
        "`manifest/hash_manifest.json` at the time it was rendered.",
        "",
        "**Legend:**",
        "- \U0001F534 red = metadata changed, template changed, or object is new "
        "(direct hit)",
        "- \U0001F7E1 yellow = unchanged itself, pulled in because something it "
        "`depends_on` changed (propagated impact)",
        "- \U0001F7E2 green = untouched, hash matches the manifest, not regenerated",
        "",
        "```mermaid",
        mermaid_block,
        "```",
        "",
    ]
    if changed_or_new or downstream_only:
        summary.append(
            f"**Pending:** {len(changed_or_new)} changed/new, "
            f"{len(downstream_only)} pulled in downstream, "
            f"{len(unaffected)} untouched. Run `python run.py diff` then "
            f"`python run.py generate` to clear this."
        )
    else:
        summary.append("**Clean:** metadata matches the last successful build. "
                        "Nothing pending.")

    OUT_PATH.write_text("\n".join(summary) + "\n")
    return OUT_PATH


if __name__ == "__main__":
    print(render())
